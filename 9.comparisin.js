// Greater than
console.log(5>6);
// Lesser than
console.log(5<6);
// Equal 
console.log(5==6);
// Greater or Equal
console.log(5>=6);
// Lesser or Equal
console.log(5<=6);
// Not Equal
console.log(5!=6);

// || stands for OR operation and && stands for AND operation
console.log(5<6 || 6<5);
console.log(5<6 && 6<5);
console.log("Here OR operation is true but AND operation is false, why?")
